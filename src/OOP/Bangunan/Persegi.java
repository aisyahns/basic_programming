package OOP.Bangunan;

public class Persegi {

    public int sisi; //attribute dari kategori atau class bangunan persegi

    public int keliling(){ //method dari class persegi yang mana perhitungannya sesuai dengan bangunannya
        return sisi * 4;
    }

    public int luas(){
        return sisi * sisi;
    }
}
